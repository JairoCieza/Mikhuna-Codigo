
package Model;

public class Mesa {
    private String codigoM;
    private String nombreM;
    private int capacidad;
    private String ubicacionM;

    public String getCodigoM() {
        return codigoM;
    }

    public void setCodigoM(String codigoM) {
        this.codigoM = codigoM;
    }

    public String getNombreM() {
        return nombreM;
    }

    public void setNombreM(String nombreM) {
        this.nombreM = nombreM;
    }

    public int getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(int capacidad) {
        this.capacidad = capacidad;
    }

    public String getUbicacionM() {
        return ubicacionM;
    }

    public void setUbicacionM(String ubicacionM) {
        this.ubicacionM = ubicacionM;
    }
    
    
}
